# Thin CLI wrapper around fastgs.surface.mesh.render_sets (Fast-PGSR mesh extraction).
# Requires the `fastgs` package to be installed (e.g. `uv pip install -e .`).

import torch
from argparse import ArgumentParser

from fastgs.surface.mesh import render_sets
from fastgs.surface.utils.general_utils import safe_state
from fastgs.surface.arguments import ModelParams, PipelineParams, get_combined_args


def main():
    torch.set_num_threads(8)
    # Set up command line argument parser
    parser = ArgumentParser(description="Testing script parameters")
    model = ModelParams(parser, sentinel=True)
    pipeline = PipelineParams(parser)
    parser.add_argument("--iteration", default=-1, type=int)
    parser.add_argument("--skip_train", action="store_true")
    parser.add_argument("--skip_test", action="store_true")
    parser.add_argument("--quiet", action="store_true")
    parser.add_argument("--max_depth", default=5.0, type=float)
    parser.add_argument("--voxel_size", default=0.002, type=float)
    parser.add_argument("--num_cluster", default=1, type=int)
    parser.add_argument("--mult", type=float, default=0.5)
    parser.add_argument("--use_depth_filter", action="store_true")

    args = get_combined_args(parser)
    print("Rendering " + args.model_path)

    # Initialize system state (RNG)
    safe_state(args.quiet)
    print(f"multi_view_num {model.multi_view_num}")
    render_sets(model.extract(args), args.iteration, pipeline.extract(args), args.skip_train, args.skip_test, args.max_depth, args.voxel_size, args.num_cluster, args.use_depth_filter, args)


if __name__ == "__main__":
    main()
