# Thin CLI wrapper around fastgs.rendering.renderer.render_sets.
# Requires the `fastgs` package to be installed (e.g. `uv pip install -e .`).

from argparse import ArgumentParser

from fastgs.rendering.renderer import render_sets
from fastgs.rendering.utils.general_utils import safe_state
from fastgs.rendering.arguments import ModelParams, PipelineParams, get_combined_args


def main():
    # Set up command line argument parser
    parser = ArgumentParser(description="Testing script parameters")
    model = ModelParams(parser, sentinel=True)
    pipeline = PipelineParams(parser)
    parser.add_argument("--iteration", default=-1, type=int)
    parser.add_argument("--skip_train", action="store_true")
    parser.add_argument("--skip_test", action="store_true")
    parser.add_argument("--quiet", action="store_true")
    parser.add_argument("--mult", type=float, default=0.5)
    args = get_combined_args(parser)
    print("Rendering " + args.model_path)

    # Initialize system state (RNG)
    safe_state(args.quiet)

    render_sets(model.extract(args), args.iteration, pipeline.extract(args), args.skip_train, args.skip_test, args)


if __name__ == "__main__":
    main()
